#Calculadora de consumo de combustivel

distacia_pecorrida = 300
combustivel_gasto = 25

#calculo do consumo de gasolina
consumo_medio = distacia_pecorrida / combustivel_gasto

#saida da distancia pecorrida
print(f"A distancia pecorrida foi de: {distacia_pecorrida:.2f}")

#saida do combustivel gasto na viagem
print(f"O combustivel gasto na viagem foi de: {combustivel_gasto:.2f}")

#saida do consumo medio
print(f"O consumo medio de gasolina foi de: {consumo_medio:.2f}")