#Media escolar

nota1 = 7.5
nota2 = 8.0
nota3 = 6.5

#Calculo da media
media = (nota1 + nota2 + nota3) / 3

#saida das notas do aluno da 1ª ate a 3ª
print(f"A primeira nota do aluno foi {nota1}")
print(f"A segunda nota do aluno foi {nota2}")
print(f"A terceira nota do aluno foi {nota3}")

#saida da media
print(f"A media geral do aluno foi: {media:.2f}")